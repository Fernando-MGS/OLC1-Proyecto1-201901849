package ContenedorJS;

/**
 *
 * @author ferna
 */
public class Variables {
    private String id;

    
    public Variables(){
        
    }
    
    public Variables(String id){
        this.id=id;
    }
    
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }
    
    
}
